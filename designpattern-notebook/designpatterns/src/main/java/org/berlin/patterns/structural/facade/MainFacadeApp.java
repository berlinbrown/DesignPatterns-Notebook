package org.berlin.patterns.structural.facade;

/**
 * Structural Pattern : Composition of smaller objects to compose larger objects. 
 */


/**
 * Difference between adapter and facade:
 *  
 * "An Adapter is used when the wrapper must respect a particular interface and must support a polymorphic behavior. 
 * On the other hand, a facade is used when one wants an easier or simpler interface to work with."
 * 
 * Facade, simpler interface
 * 
 * Source:
 * http://en.wikipedia.org/wiki/Facade_pattern
 */
public class MainFacadeApp {

	public static void main(final String [] args) {
		
		System.out.println("Running");
		System.out.println("Done");
		
	} // End of the method //
	
} // End of the class //
